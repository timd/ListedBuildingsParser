<h1>Listed Buildings Parser</h1>
<p>Accepts requests in the form:</p>
<p><code>query.php?lat=nnnnnn&lng=nnnnnn&radius=mmmm[&full="no"][&html="true"]</code></p>
<p>where</p>
<p><code>nnnnnn</code> = latitude/longitude coordinates</p>
<p><code>mmmm</code> = search radius in metres</p>
<p><code>full</code> = toggles display of full or lat/long only</p>
<p><code>html</code> = toggles display in html</p>
